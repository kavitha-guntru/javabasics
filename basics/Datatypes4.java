/* Define variables for different Data Types int, Boolean, char, float, double and print on the  
      Console  */
package basics;

public class Datatypes4 
{
        public static void main(String[] args) 
        {
            int integerVar = 25;
            boolean booleanVar = true;
            char charVar = 'A';
            float floatVar = 5.75f;
            double doubleVar = 19.99;
            System.out.println("Integer: " + integerVar);
            System.out.println("Boolean: " + booleanVar);
            System.out.println("Character: " + charVar);
            System.out.println("Float: " + floatVar);
            System.out.println("Double: " + doubleVar);
        }
}
    
    
