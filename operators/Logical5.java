package operators;
public class Logical5 {
        public static void main(String[] args) {
            boolean a = true;
            boolean b = false;
            System.out.println("Logical AND (a && b): " + (a && b));
            System.out.println("Logical OR (a || b): " + (a || b));
            System.out.println("Logical NOT (!a): " + (!a));
        }
}
    
    

