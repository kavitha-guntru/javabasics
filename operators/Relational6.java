package operators;

public class Relational6 {
        public static void main(String[] args) {
            int x = 15;
            int y = 20;
            System.out.println("x < y: " + (x < y));
            System.out.println("x <= y: " + (x <= y));
            System.out.println("x > y: " + (x > y));
            System.out.println("x >= y: " + (x >= y));
        }
}
    
